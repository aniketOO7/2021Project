#include <iostream>
#include <string>
using namespace std;
int main(){
	cout<<"char size is "<<sizeof(char)<<"\n";
	cout<<"short size is "<<sizeof(short)<<"\n";
	cout<<"int size is "<<sizeof(int)<<"\n";
	cout<<"long size is "<<sizeof(long)<<"\n";
	cout<<"long long size is "<<sizeof(long long)<<"\n";
	cout<<"float size is "<<sizeof(float)<<"\n";
	cout<<"double size is "<<sizeof(double)<<"\n";
	cout<<"long double size is"<<sizeof(long double)<<"\n";
	cout<<"bool size is "<<sizeof(bool)<<"\n";
	return 0;
}
